fn isCodePointInGcMask(code_point: u21, gc_mask: u32) bool {
    var p: []const u8 = &unicode_gc_table;
    var c: u32 = 0;
    while (p.len > 0) {
        const b = p[0];
        p = p[1..];
        var n: u32 = b >> 5;
        const v: u32 = b & 0x1f;

        if (n == 7) {
            const next_b = p[0];
            p = p[1..];
            if (next_b < 128) {
                n = next_b + 7;
            } else if (next_b < 128 + 64) {
                n = @as(u32, next_b - 128) << 8;
                n |= p[0];
                p = p[1..];
                n += 7 + 128;
            } else {
                n = @as(u32, next_b - 128 - 64) << 16;
                n |= @as(u32, p[0]) << 8;
                n |= p[1];
                p = p[2..];
                n += 7 + 128 + (1 << 14);
            }
        }

        const c0 = c;
        c += n + 1;

        if (code_point >= c0 and code_point < c) {
            if (v == 31) {
                const upper_lower = gc_mask & (lu_mask | ll_mask);
                if (upper_lower != 0) {
                    if (upper_lower == (lu_mask | ll_mask)) {
                        return true;
                    } else {
                        var temp_c0 = c0;
                        if ((gc_mask & ll_mask) != 0) temp_c0 += 1;
                        return (code_point - temp_c0) % 2 == 0;
                    }
                }
            } else if (((gc_mask >> @intCast(v)) & 1) != 0) {
                return true;
            }
            break;
        }
    }
    return false;
}

fn isCodePointInPropTable(code_point: u21, prop_idx: usize) bool {
    if (prop_idx >= data.unicode_prop_table.len) return false;
    const table = data.unicode_prop_table[prop_idx];
    var p: usize = 0;
    var c: u32 = 0;
    var bit = false;
    while (p < table.len) {
        var c0 = c;
        const b = table[p];
        p += 1;
        if (b < 64) {
            c += (b >> 3) + 1;
            if (bit and code_point >= c0 and code_point < c) return true;
            bit = !bit;
            c0 = c;
            c += (b & 7) + 1;
        } else if (b >= 0x80) {
            c += b - 0x80 + 1;
        } else if (b < 0x60) {
            c += ((@as(u32, b - 0x40) << 8) | table[p]) + 1;
            p += 1;
        } else {
            c += ((@as(u32, b - 0x60) << 16) | (@as(u32, table[p]) << 8) | table[p + 1]) + 1;
            p += 2;
        }
        if (bit and code_point >= c0 and code_point < c) return true;
        bit = !bit;
    }
    return false;
}

fn isCodePointInCaseRanges(code_point: u21, case_mask: u32) bool {
    if (case_mask == 0) return false;
