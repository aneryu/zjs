            switch (typ) {
                RUN_TYPE_UL => {
                    if ((case_mask & CASE_U) != 0 and (case_mask & (CASE_L | CASE_F)) != 0) {
                        return true;
                    } else {
                        const offset = if ((case_mask & CASE_U) != 0) @as(u32, 1) else 0;
                        return ((code_point - code) % 2) == offset;
                    }
                },
                RUN_TYPE_LSU => {
                    if ((case_mask & CASE_U) != 0 and (case_mask & (CASE_L | CASE_F)) != 0) {
                        return true;
                    } else {
                        if ((case_mask & CASE_U) == 0) {
                            if (code_point == code or code_point == code + 1) return true;
                        } else {
                            if (code_point == code + 1 or code_point == code + 2) return true;
                        }
                        return false;
                    }
                },
                else => return true,
            }
        }
    }
    return false;
}

fn isCodePointInProperty(code_point: u21, prop_idx: usize) bool {
    switch (prop_idx) {
        data.Prop.ASCII => return code_point < 0x80,
        data.Prop.Any => return code_point < 0x110000,
        data.Prop.Assigned => return !isCodePointInGcMask(code_point, cn_mask),
        data.Prop.Math => return isCodePointInGcMask(code_point, sm_mask) or isCodePointInPropTable(code_point, data.Prop.Other_Math),
        data.Prop.Lowercase => return isCodePointInGcMask(code_point, ll_mask) or isCodePointInPropTable(code_point, data.Prop.Other_Lowercase),
        data.Prop.Uppercase => return isCodePointInGcMask(code_point, lu_mask) or isCodePointInPropTable(code_point, data.Prop.Other_Uppercase),
        data.Prop.Cased => {
            return isCodePointInGcMask(code_point, lu_mask | ll_mask | lt_mask) or
                isCodePointInPropTable(code_point, data.Prop.Other_Uppercase) or
                isCodePointInPropTable(code_point, data.Prop.Other_Lowercase);
        },
        data.Prop.Alphabetic => {
            return isCodePointInGcMask(code_point, lu_mask | ll_mask | lt_mask | lm_mask | lo_mask | (@as(u32, 1) << @intCast(data.GC.Nl))) or
                isCodePointInPropTable(code_point, data.Prop.Other_Uppercase) or
                isCodePointInPropTable(code_point, data.Prop.Other_Lowercase) or
                isCodePointInPropTable(code_point, data.Prop.Other_Alphabetic);
        },
        data.Prop.Grapheme_Base => {
            const base_gc = cc_mask | cf_mask | cs_mask | co_mask | cn_mask | zl_mask | zp_mask | me_mask | mn_mask;
            return !isCodePointInGcMask(code_point, base_gc) and !isCodePointInPropTable(code_point, data.Prop.Other_Grapheme_Extend);
        },
        data.Prop.Grapheme_Extend => return isCodePointInGcMask(code_point, me_mask | mn_mask) or isCodePointInPropTable(code_point, data.Prop.Other_Grapheme_Extend),
        data.Prop.ID_Start => {
            const id_start_gc = lu_mask | ll_mask | lt_mask | lm_mask | lo_mask | (@as(u32, 1) << @intCast(data.GC.Nl));
            return isCodePointInGcMask(code_point, id_start_gc) or isCodePointInPropTable(code_point, data.Prop.Other_ID_Start);
        },
        data.Prop.ID_Continue => {
            const id_start_gc = lu_mask | ll_mask | lt_mask | lm_mask | lo_mask | (@as(u32, 1) << @intCast(data.GC.Nl));
            const id_continue_gc = id_start_gc | mn_mask | mc_mask | nd_mask | pc_mask;
            return isCodePointInGcMask(code_point, id_continue_gc) or
                isCodePointInPropTable(code_point, data.Prop.Other_ID_Start) or
                isCodePointInPropTable(code_point, data.Prop.Other_ID_Continue);
        },
        data.Prop.XID_Start => {
            const base_gc = lu_mask | ll_mask | lt_mask | lm_mask | lo_mask | (@as(u32, 1) << @intCast(data.GC.Nl));
            const is_allowed = isCodePointInGcMask(code_point, base_gc) or isCodePointInPropTable(code_point, data.Prop.Other_ID_Start);
            const is_excluded = isCodePointInPropTable(code_point, data.Prop.Pattern_Syntax) or
                isCodePointInPropTable(code_point, data.Prop.Pattern_White_Space) or
                isCodePointInPropTable(code_point, data.Prop.XID_Start1);
            return is_allowed and !is_excluded;
        },
        data.Prop.XID_Continue => {
            const base_gc = lu_mask | ll_mask | lt_mask | lm_mask | lo_mask | (@as(u32, 1) << @intCast(data.GC.Nl)) | mn_mask | mc_mask | nd_mask | pc_mask;
            const is_allowed = isCodePointInGcMask(code_point, base_gc) or
                isCodePointInPropTable(code_point, data.Prop.Other_ID_Start) or
                isCodePointInPropTable(code_point, data.Prop.Other_ID_Continue);
            const is_excluded = isCodePointInPropTable(code_point, data.Prop.Pattern_Syntax) or
                isCodePointInPropTable(code_point, data.Prop.Pattern_White_Space) or
                isCodePointInPropTable(code_point, data.Prop.XID_Continue1);
            return is_allowed and !is_excluded;
        },
        data.Prop.Changes_When_Titlecased => return isCodePointInCaseRanges(code_point, CASE_U) != isCodePointInPropTable(code_point, data.Prop.Changes_When_Titlecased1),
        data.Prop.Changes_When_Casefolded => return isCodePointInCaseRanges(code_point, CASE_F) != isCodePointInPropTable(code_point, data.Prop.Changes_When_Casefolded1),
        data.Prop.Changes_When_NFKC_Casefolded => return isCodePointInCaseRanges(code_point, CASE_F) != isCodePointInPropTable(code_point, data.Prop.Changes_When_NFKC_Casefolded1),
        data.Prop.Changes_When_Uppercased => return isCodePointInCaseRanges(code_point, CASE_U),
        data.Prop.Changes_When_Lowercased => return isCodePointInCaseRanges(code_point, CASE_L),
        data.Prop.Changes_When_Casemapped => return isCodePointInCaseRanges(code_point, CASE_U | CASE_L | CASE_F),
        else => return isCodePointInPropTable(code_point, prop_idx),
    }
}

fn unicode_script(code_point: u21, script_idx: u32, is_ext: bool) bool {
    const is_common = script_idx == data.Script.Common or script_idx == data.Script.Inherited;
    var p: []const u8 = &data.unicode_script_table;
    var c: u32 = 0;
    var primary_match = false;
    var found_range = false;
    while (p.len > 0) {
        const b = p[0];
        p = p[1..];
        const type_bit = b >> 7;
        var n: u32 = b & 0x7f;
        if (n < 96) {
            // no-op
        } else if (n < 112) {
            n = (n - 96) << 8;
            n |= p[0];
            p = p[1..];
            n += 96;
        } else {
            n = (n - 112) << 16;
            n |= @as(u32, p[0]) << 8;
            n |= p[1];
            p = p[2..];
            n += 96 + (1 << 12);
        }

        var v: u32 = 0;
        if (type_bit != 0) {
            v = p[0];
            p = p[1..];
        }

        const c1 = c + n + 1;
        if (code_point >= c and code_point < c1) {
            found_range = true;
            if (v == script_idx) {
                primary_match = true;
            }
            break;
        }
        c = c1;
    }
    if (!found_range and code_point >= c and code_point <= max_code_point and script_idx == data.Script.Unknown) {
        primary_match = true;
    }

    if (!is_ext) {
        return primary_match;
    }

    var p_ext: []const u8 = &unicode_script_ext_table;
    c = 0;
    var ext_match = false;
    var ext_has_any = false;
    while (p_ext.len > 0) {
        const b = p_ext[0];
        p_ext = p_ext[1..];
        var n: u32 = 0;
        if (b < 128) {
            n = b;
        } else if (b < 128 + 64) {
            n = @as(u32, b - 128) << 8;
            n |= p_ext[0];
            p_ext = p_ext[1..];
            n += 128;
        } else {
            n = @as(u32, b - 128 - 64) << 16;
            n |= @as(u32, p_ext[0]) << 8;
            n |= p_ext[1];
            p_ext = p_ext[2..];
            n += 128 + (1 << 14);
        }

        const c1 = c + n + 1;
        const v_len = p_ext[0];
        p_ext = p_ext[1..];

        if (code_point >= c and code_point < c1) {
            if (is_common) {
                if (v_len != 0) {
                    ext_has_any = true;
                }
            } else {
                for (p_ext[0..v_len]) |val| {
                    if (val == script_idx) {
                        ext_match = true;
                        break;
                    }
                }
            }
            break;
        }

        p_ext = p_ext[v_len..];
        c = c1;
    }

    if (is_common) {
        return primary_match and !ext_has_any;
    } else {
        return primary_match or ext_match;
    }
}

// --- Public Interface ---

pub fn isUnicodePropertyMatches(code_point: u21, name: []const u8) bool {
    // 1. Resolve alias target
    if (exactScriptExtensionsAliasTarget(name)) |target| {
        return isUnicodePropertyMatches(code_point, target);
    }

    // 2. Parse property category prefixes
    var is_script = false;
    var is_ext = false;
    var name_str = name;
    if (std.mem.startsWith(u8, name, "Script=")) {
        is_script = true;
        name_str = name[7..];
    } else if (std.mem.startsWith(u8, name, "sc=")) {
        is_script = true;
        name_str = name[3..];
    } else if (std.mem.startsWith(u8, name, "Script_Extensions=")) {
        is_script = true;
        is_ext = true;
        name_str = name[18..];
    } else if (std.mem.startsWith(u8, name, "scx=")) {
        is_script = true;
        is_ext = true;
        name_str = name[4..];
    }

    if (is_script) {
        if (scriptIndex(name_str)) |script_idx| {
            return unicode_script(code_point, script_idx, is_ext);
        }
        return false;
    }

    var is_gc = false;
    if (std.mem.startsWith(u8, name, "General_Category=")) {
        is_gc = true;
        name_str = name[17..];
    } else if (std.mem.startsWith(u8, name, "gc=")) {
        is_gc = true;
        name_str = name[3..];
    }

    if (is_gc) {
        if (GcMap.get(name_str)) |gc_idx| {
            const mask = if (gc_idx < data.GC.LC)
                @as(u32, 1) << @intCast(gc_idx)
            else
                gc_mask_table[gc_idx - data.GC.LC];
            return isCodePointInGcMask(code_point, mask);
        }
        return false;
    }

    // 3. Fallback: bare property names (order of preference: GC, bare Script as scx, Binary Prop)
    if (GcMap.get(name)) |gc_idx| {
        const mask = if (gc_idx < data.GC.LC)
            @as(u32, 1) << @intCast(gc_idx)
        else
            gc_mask_table[gc_idx - data.GC.LC];
        return isCodePointInGcMask(code_point, mask);
    }

    if (scriptIndex(name)) |script_idx| {
        return unicode_script(code_point, script_idx, true);
    }

    if (PropMap.get(name)) |found| {
        const prop_idx = found + data.Prop.ASCII_Hex_Digit;
        return isCodePointInProperty(code_point, prop_idx);
    }

    return false;
}

